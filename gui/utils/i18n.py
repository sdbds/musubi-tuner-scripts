class I18n:
    """Internationalization helper class with binding support"""
    
    def __init__(self, lang: str = 'zh'):
        self._lang = lang if lang in TRANSLATIONS else 'zh'
        self._bindings: List[Callable] = []
    
    @property
    def lang(self) -> str:
        return self._lang
    
    @lang.setter
    def lang(self, value: str):
        old_lang = self._lang
        self._lang = value if value in TRANSLATIONS else 'zh'
        if old_lang != self._lang:
            self._notify_bindings()
    
    def t(self, key: str, default: str = None) -> str:
        """Get translated text by key"""
        return TRANSLATIONS.get(self._lang, TRANSLATIONS['zh']).get(key, default or key)
    
    def bind(self, callback: Callable):
        """Register a callback to be called when language changes"""
        self._bindings.append(callback)
        # Immediately call with current translation
        callback()
    
    def unbind(self, callback: Callable):
        """Unregister a callback"""
        if callback in self._bindings:
            self._bindings.remove(callback)
    
    def _notify_bindings(self):
        """Notify all registered callbacks"""
        for callback in self._bindings:
            try:
                callback()
            except Exception:
                pass
    
    def get_available_langs(self) -> Dict[str, str]:
        """Get available languages"""
        return {
            'en': 'English',
            'zh': '中文',
            'ja': '日本語',
            'ko': '한국어',
        }


class TranslatableLabel:
    """A translatable label that updates when language changes"""
    
    def __init__(self, key: str, default: str = None, **kwargs):
        self.key = key
        self.default = default
        self.kwargs = kwargs
        self._label = None
        self._element = None
    
    def create(self, parent=None):
        """Create the label element"""
        from nicegui import ui
        if parent:
            with parent:
                self._label = ui.label(self._get_text())
        else:
            self._label = ui.label(self._get_text())
        
        # Apply any additional props/styles
        for key, value in self.kwargs.items():
            if key == 'classes':
                self._label.classes(value)
            elif key == 'style':
                self._label.style(value)
        
        # Bind to language changes
        _i18n.bind(self._update)
        
        return self._label
    
    def _get_text(self) -> str:
        return _i18n.t(self.key, self.default)
    
    def _update(self):
        if self._label:
            self._label.set_text(self._get_text())


def tl(key: str, default: str = None, **kwargs):
    """Create a translatable label that updates when language changes"""
    return TranslatableLabel(key, default, **kwargs)


# Language config file
import os

LANG_CONFIG_FILE = os.path.join(os.path.dirname(__file__), '.lang_config')

def _save_lang_to_file(lang: str):
    """Save language setting to file"""
    try:
        with open(LANG_CONFIG_FILE, 'w', encoding='utf-8') as f:
            f.write(lang)
    except Exception:
        pass

def _load_lang_from_file() -> str:
    """Load language setting from file"""
    try:
        if os.path.exists(LANG_CONFIG_FILE):
            with open(LANG_CONFIG_FILE, 'r', encoding='utf-8') as f:
                lang = f.read().strip()
                if lang in TRANSLATIONS:
                    return lang
    except Exception:
        pass
    return 'zh'

# Global i18n instance with persisted language
_i18n = I18n(_load_lang_from_file())


def set_language(lang: str):
    """Set global language and persist it"""
    _i18n.lang = lang
    _save_lang_to_file(lang)


def get_i18n() -> I18n:
    """Get global i18n instance"""
    return _i18n


def _flatten_translation_strings(value: Any, prefix: tuple[str, ...] = ()) -> Dict[tuple[str, ...], str]:
    strings: Dict[tuple[str, ...], str] = {}
    if isinstance(value, str):
        strings[prefix] = value
    elif isinstance(value, dict):
        for key, item in value.items():
            strings.update(_flatten_translation_strings(item, prefix + (str(key),)))
    return strings


def get_translation_pairs(old_lang: str, new_lang: str) -> Dict[str, str]:
    """Return old visible strings mapped to their new-language equivalents."""
    old_values = _flatten_translation_strings(TRANSLATIONS.get(old_lang, TRANSLATIONS["zh"]))
    new_values = _flatten_translation_strings(TRANSLATIONS.get(new_lang, TRANSLATIONS["zh"]))
    pairs: Dict[str, str] = {}
    for path, old_text in old_values.items():
        new_text = new_values.get(path)
        if not old_text or not new_text or old_text == new_text:
            continue
        pairs.setdefault(old_text, new_text)
    return pairs


def t(key: str, default: str = None) -> str:
    """Shortcut for translation"""
    return _i18n.t(key, default)
